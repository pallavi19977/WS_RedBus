package com.WS_RedBus_Consumer.model;

public class RedBus {
	
	private int busId;
	private String busNo;
	private String busname;
	private String busFrom;
	private String busTo;
	private String busDate;
	private String busTimming;
	private float busTicket;
	public int getBusId() {
		return busId;
	}
	public void setBusId(int busId) {
		this.busId = busId;
	}
	public String getBusNo() {
		return busNo;
	}
	public void setBusNo(String busNo) {
		this.busNo = busNo;
	}
	public String getBusname() {
		return busname;
	}
	public void setBusname(String busname) {
		this.busname = busname;
	}
	public String getBusFrom() {
		return busFrom;
	}
	public void setBusFrom(String busFrom) {
		this.busFrom = busFrom;
	}
	public String getBusTo() {
		return busTo;
	}
	public void setBusTo(String busTo) {
		this.busTo = busTo;
	}
	public String getBusDate() {
		return busDate;
	}
	public void setBusDate(String busDate) {
		this.busDate = busDate;
	}
	public String getBusTimming() {
		return busTimming;
	}
	public void setBusTimming(String busTimming) {
		this.busTimming = busTimming;
	}
	public float getBusTicket() {
		return busTicket;
	}
	public void setBusTicket(float busTicket) {
		this.busTicket = busTicket;
	}
	
	

}
